/**
 * @param {character[][]} board
 * @return {number}
 */
var numRookCaptures = function (board) {
    let a = 0,
        b = 0,
        ret = 0;

    findR: {
        for (let i = 0; i < 8; i++) {
            for (let j = 0; j < 8; j++) {
                if (board[i][j] === "R") {
                    a = i;
                    b = j;
                    break findR;
                }
            }
        }
    }
    // Up
    for (let i = a - 1; i >= 0; i--) {
        if (board[i][b] === "B") {
            break;
        } else if (board[i][b] === "p") {
            ret++;
            break;
        }
    }
    // Down
    for (let i = a + 1; i < 8; i++) {
        if (board[i][b] === "B") {
            break;
        } else if (board[i][b] === "p") {
            ret++;
            break;
        }
    }
    // Left
    for (let j = b - 1; j >= 0; j--) {
        if (board[a][j] === "B") {
            break;
        } else if (board[a][j] === "p") {
            ret++;
            break;
        }
    }
    // Right
    for (let j = b + 1; j < 8; j++) {
        if (board[a][j] === "B") {
            break;
        } else if (board[a][j] === "p") {
            ret++;
            break;
        }
    }
    return ret;
};

